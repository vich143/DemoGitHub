public class MyCalc {

    public static int add(int a, int b) {
        return a + b;
    }

    public static int minus(int a, int b) {
        return a + b;
    }

    public static int multi(int a, int b) {
        return a * b;
    }

    public static int div(int a, int b) {
        return a / b;
    }
}
